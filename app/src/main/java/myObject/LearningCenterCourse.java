package myObject;

//課程
public class LearningCenterCourse {
    private String ID; //課程ID
    private String LEARNINGCENTER; //樂齡中心
    private String COURSESACTIVITIES; //課程/活動名稱
    private String DATE; //日期
    private String LOCATION; //地點
    private String LECTURER; //講師
    private String REGISTRATIONSITUATION; //報名情形
    private String COST; //報名費用
    private String ADDRESS; //地址
    private String PHONENUMBER; //聯絡電話

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getLEARNINGCENTER() {
        return LEARNINGCENTER;
    }

    public void setLEARNINGCENTER(String LEARNINGCENTER) {
        this.LEARNINGCENTER = LEARNINGCENTER;
    }

    public String getCOURSESACTIVITIES() {
        return COURSESACTIVITIES;
    }

    public void setCOURSESACTIVITIES(String COURSESACTIVITIES) {
        this.COURSESACTIVITIES = COURSESACTIVITIES;
    }

    public String getDATE() {
        return DATE;
    }

    public void setDATE(String DATE) {
        this.DATE = DATE;
    }

    public String getLOCATION() {
        return LOCATION;
    }

    public void setLOCATION(String LOCATION) {
        this.LOCATION = LOCATION;
    }

    public String getLECTURER() {
        return LECTURER;
    }

    public void setLECTURER(String LECTURER) {
        this.LECTURER = LECTURER;
    }

    public String getREGISTRATIONSITUATION() {
        return REGISTRATIONSITUATION;
    }

    public void setREGISTRATIONSITUATION(String REGISTRATIONSITUATION) {
        this.REGISTRATIONSITUATION = REGISTRATIONSITUATION;
    }

    public String getCOST() {
        return COST;
    }

    public void setCOST(String COST) {
        this.COST = COST;
    }

    public String getADDRESS() {
        return ADDRESS;
    }

    public void setADDRESS(String ADDRESS) {
        this.ADDRESS = ADDRESS;
    }

    public String getPHONENUMBER() {
        return PHONENUMBER;
    }

    public void setPHONENUMBER(String PHONENUMBER) {
        this.PHONENUMBER = PHONENUMBER;
    }
}
